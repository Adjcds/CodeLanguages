# operadores_aritmeticos.py
# Exemplos de operadores aritméticos em Python

a = 15
b = 4

# Operações básicas
soma = a + b  # Adição
subtracao = a - b  # Subtração
multiplicacao = a * b  # Multiplicação
divisao = a / b  # Divisão (resultado em float)
divisao_inteira = a // b  # Divisão Inteira (resultado em int)
modulo = a % b  # Módulo (resto da divisão)
potencia = a ** b  # Potência (a elevado a b)

print("Operadores Aritméticos:")
print("Soma:", soma)  # Exibe a soma
print("Subtração:", subtracao)  # Exibe a subtração
print("Multiplicação:", multiplicacao)  # Exibe a multiplicação
print("Divisão:", divisao)  # Exibe a divisão
print("Divisão Inteira:", divisao_inteira)  # Exibe a divisão inteira
print("Módulo:", modulo)  # Exibe o módulo
print("Potência:", potencia)  # Exibe a potência
