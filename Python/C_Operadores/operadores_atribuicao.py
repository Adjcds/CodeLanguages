# Exemplos de operadores de atribuição em Python

z = 10
print("Valor inicial de z:", z)  # Exibe o valor inicial de z

z += 5  # Atribuição com adição (z = z + 5)
print("Após z += 5:", z)  # Exibe o valor de z após a adição

z -= 3  # Atribuição com subtração (z = z - 3)
print("Após z -= 3:", z)  # Exibe o valor de z após a subtração

z *= 2  # Atribuição com multiplicação (z = z * 2)
print("Após z *= 2:", z)  # Exibe o valor de z após a multiplicação

z /= 4  # Atribuição com divisão (z = z / 4)
print("Após z /= 4:", z)  # Exibe o valor de z após a divisão

z %= 3  # Atribuição com módulo (z = z % 3)
print("Após z %= 3:", z)  # Exibe o valor de z após o módulo

z **= 2  # Atribuição com potência (z = z ** 2)
print("Após z **= 2:", z)  # Exibe o valor de z após a potência

z //= 3  # Atribuição com divisão inteira (z = z // 3)
print("Após z //= 3:", z)  # Exibe o valor de z após a divisão inteira
