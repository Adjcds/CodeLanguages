# Exemplos de operadores de comparação em Python

x = 10
y = 20

# Comparações básicas
igual = (x == y)  # Verifica se x é igual a y
diferente = (x != y)  # Verifica se x é diferente de y
maior = (x > y)  # Verifica se x é maior que y
menor = (x < y)  # Verifica se x é menor que y
maior_ou_igual = (x >= y)  # Verifica se x é maior ou igual a y
menor_ou_igual = (x <= y)  # Verifica se x é menor ou igual a y

print("Operadores de Comparação:")
print("Igual:", igual)  # Exibe se x é igual a y
print("Diferente:", diferente)  # Exibe se x é diferente de y
print("Maior:", maior)  # Exibe se x é maior que y
print("Menor:", menor)  # Exibe se x é menor que y
print("Maior ou Igual:", maior_ou_igual)  # Exibe se x é maior ou igual a y
print("Menor ou Igual:", menor_ou_igual)  # Exibe se x é menor ou igual a y
