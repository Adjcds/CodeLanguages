# Exemplos de operadores lógicos em Python

v = True
f = False

# Operações lógicas
e = v and f  # 'and' retorna True se ambos forem verdadeiros
ou = v or f  # 'or' retorna True se pelo menos um for verdadeiro
nao = not v  # 'not' inverte o valor lógico

print("Operadores Lógicos:")
print("E (and):", e)  # Exibe o resultado do 'and'
print("Ou (or):", ou)  # Exibe o resultado do 'or'
print("Não (not):", nao)  # Exibe o resultado do 'not'
